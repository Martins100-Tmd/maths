export const mathUtilFn = {
   sum: function (a: number, b: number) {
      return a + b;
   },
   mul: function (a: number, b: number) {
      return a * b;
   },
   div: function (a: number, b: number) {
      return Number(a / b);
   },
   sub: function (a: number, b: number) {
      return a - b;
   },
   sqr: function (a: number) {
      return a * a;
   },
};
